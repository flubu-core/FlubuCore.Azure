     public partial class AzureBatchNodeReimageTask : ExternalProcessTaskBase<AzureBatchNodeReimageTask>
     {
        
        /// <summary>
        /// Reinstalls the operating system on the specified compute node.
        /// </summary>
        public AzureBatchNodeReimageTask(string nodeId = null ,  string poolId = null)
        {
            WithArguments("az batch node reimage");
WithArguments("--node-id");
            if (!string.IsNullOrEmpty(nodeId))
            {
                 WithArguments(nodeId);
            }

WithArguments("--pool-id");
            if (!string.IsNullOrEmpty(poolId))
            {
                 WithArguments(poolId);
            }

        }

        protected override string Description { get; set; }
        
        /// <summary>
        /// When to reimage the compute node and what to do with currently running tasks. The default value is requeue.
        /// </summary>
        public AzureBatchNodeReimageTask NodeReimageOption(string nodeReimageOption = null)
        {
            WithArguments("--node-reimage-option");
            if (!string.IsNullOrEmpty(nodeReimageOption))
            {
                 WithArguments(nodeReimageOption);
            }

            return this;
        }

        /// <summary>
