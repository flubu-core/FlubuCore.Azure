
using System;
using System.Collections.Generic;
using System.Text;
using FlubuCore.Context;
using FlubuCore.Tasks;
using FlubuCore.Tasks.Process;

namespace FlubuCore.Azure.Tasks.Lab
{
     public partial class AzureLabDeleteTask : ExternalProcessTaskBase<AzureLabDeleteTask>
     {
        
        /// <summary>
        /// Delete lab.
        /// </summary>
        public AzureLabDeleteTask(string name = null ,  string resourceGroup = null)
        {
            WithArguments("az lab delete");
WithArguments("--name");
            if (!string.IsNullOrEmpty(name))
            {
                 WithArguments(name);
            }

WithArguments("--resource-group");
            if (!string.IsNullOrEmpty(resourceGroup))
            {
                 WithArguments(resourceGroup);
            }

        }

        protected override string Description { get; set; }
        
     }
}
